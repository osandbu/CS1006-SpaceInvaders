package alien.hiscore;

/**
 * Generated in the hiscores are formatted incorrectly.
 * 
 * @author Ole
 */
public class ScoreFormatException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	/**
	 * Create a new ScoreFormatException with a given message.
	 * 
	 * @param message
	 *            A message.
	 */
	public ScoreFormatException(String message) {
		super(message);
	}
}
